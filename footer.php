        });
		
        $( '.load-more a' ).click( function(e){
            e.preventDefault();
            $( this ).hide();
            $( '.to-load' ).removeClass('d-none');
        } );
        $( '#filter' ).click( function(e){
            e.preventDefault();
            $( '#filter-content' ).removeClass( 'd-none' ).addClass( 'd-block' );
        } );
        $( '#close-filter' ).click( function(e){
            e.preventDefault();
            $( '#filter-content' ).removeClass( 'd-block' ).addClass( 'd-none' );
        } );
        $( '#category-load-more' ).click( function(e){
            e.preventDefault();
            $( this ).parent().find('.products').find('.product').css('display', 'block');
            $( this ).parent().find('.woocommerce-pagination').css('display', 'block');
            $( this ).removeClass('d-block').addClass('d-none');
        } );
        $( '#checkout-cart-details h4' ).click( function(event){
            $(this).parent().find('tbody').toggle();
            if ( $(this).find('span').text() == '+' ) {
                $(this).find('span').text('-');
            } else {
                $(this).find('span').text('+');
            }
        } );
		$( '.flex-active' ).parent().addClass('active-li');
